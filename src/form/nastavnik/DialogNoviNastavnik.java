    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        Nastavnik izabran = (Nastavnik) Session.getInstance().getParametriSK().get("nastavnik");
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        String email = txtEmail.getText().trim();
        String password = izabran.getPassword();
        if (pswPassword.isVisible()) {
            password = new String(pswPassword.getPassword());
        }
        String konsultacije = txtKonsultacije.getText().trim();
        String kabinet = txtKabinet.getText().trim();
        Katedra katedra = (Katedra) cmbKatedra.getSelectedItem();
        String pozicija = txtPozicija.getText().trim();
        Nastavnik novi = new Nastavnik(izabran.getNastavnikId(), ime, prezime, email, password, pozicija, konsultacije, kabinet, katedra);
        if (Validator.getInstance().nastavnikOK(novi)) {
            try {
                String poruka = Controller.getInstance().azurirajNastavnika(novi);
                FormPretraziNastavnike fpn = (FormPretraziNastavnike) this.getParent();
                fpn.updateNastavnik(novi);
                JOptionPane.showMessageDialog(this, poruka);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(DialogNoviNastavnik.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Podaci nisu dobro uneti.");
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

