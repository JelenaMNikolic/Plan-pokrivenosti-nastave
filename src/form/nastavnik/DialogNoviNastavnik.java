    private void populateCmbKatedra() {
        try {
            cmbKatedra.removeAllItems();
            List<Katedra> katedre = Controller.getInstance().getAllKatedre();
            for (Katedra katedra : katedre) {
                cmbKatedra.addItem(katedra);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    private void postaviKomponente(FormMode mode) {
        switch (mode) {
            case NOVI:
                btnIzmeni.setVisible(false);
                btnDodajNastavnika.setVisible(true);
                btnPrikazi.setVisible(false);
                setTitle("Novi nastavnik");
                break;
            case IZMENI:
                btnIzmeni.setVisible(true);
                btnDodajNastavnika.setVisible(false);
                btnPrikazi.setVisible(false);
                setSifra();
                setNastavnik();
                setTitle("Izmena nastavnika");
                break;
            case PRIKAZI:
                btnPrikazi.setVisible(true);
                btnIzmeni.setVisible(false);
                btnDodajNastavnika.setVisible(false);
                lblPass.setVisible(false);
                pswPassword.setVisible(false);
                disableFields();
                setNastavnik();
                setTitle("Prikaz nastavnika");
                break;
        }
    }

    private void setNastavnik() {
        if (!Session.getInstance().getParametriSK().isEmpty()) {
            Nastavnik nastavnik = (Nastavnik) Session.getInstance().getParametriSK().get("nastavnik");
            prikaziNastavnika(nastavnik);
        } else {
            JOptionPane.showMessageDialog(this, "Nastavnik nije izabran.");
        }
    }

    private void prikaziNastavnika(Nastavnik nastavnik) {
        txtIme.setText(nastavnik.getIme());
        txtPrezime.setText(nastavnik.getPrezime());
        txtEmail.setText(nastavnik.geteMail());
        pswPassword.setText(nastavnik.getPassword());
        cmbKatedra.setSelectedItem(nastavnik.getKatedra());
        txtPozicija.setText(nastavnik.getPozicija());
        txtKonsultacije.setText(nastavnik.getKonsultacije());
        txtKabinet.setText(nastavnik.getKabinet());
    }

    private void disableFields() {
        txtEmail.setEditable(false);
        txtIme.setEditable(false);
        txtKabinet.setEditable(false);
        txtKonsultacije.setEditable(false);
        txtPozicija.setEditable(false);
        txtPrezime.setEditable(false);
        cmbKatedra.setEnabled(false);
    }

    private void setSifra() {
        if (!Session.getInstance().getParametriSK().get("nastavnik").equals(Session.getInstance().getUlogovan())) {
            pswPassword.setVisible(false);
            lblPass.setVisible(false);
        }
    }
