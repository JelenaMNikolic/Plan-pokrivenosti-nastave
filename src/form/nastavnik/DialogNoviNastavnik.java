    private void btnDodajNastavnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajNastavnikaActionPerformed
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        String email = txtEmail.getText().trim();
        String sifra = new String(pswPassword.getPassword());
        Katedra katedra = (Katedra) cmbKatedra.getSelectedItem();
        String pozicija = txtPozicija.getText().trim();
        String kabinet = txtKabinet.getText().trim();
        String konsultacije = txtKonsultacije.getText().trim();
        Nastavnik nastavnik = new Nastavnik(-1, ime, prezime, email, sifra, pozicija, konsultacije, kabinet, katedra);
        if (Validator.getInstance().nastavnikOK(nastavnik)) {
            try {
                int id = Controller.getInstance().saveNastavnik(nastavnik);
                JOptionPane.showMessageDialog(this, "Nastavnik je sacuvan.");
                nastavnik.setNastavnikId(id);
                FormPretraziNastavnike fpn = new FormPretraziNastavnike();
                fpn.addNastavnik(nastavnik);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(DialogNoviNastavnik.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Podaci nisu dobro uneti.");
        }
    }//GEN-LAST:event_btnDodajNastavnikaActionPerformed

