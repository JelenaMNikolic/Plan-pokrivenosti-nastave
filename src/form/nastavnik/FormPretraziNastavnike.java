    private void populateTable() {
        try {
            TableNastavnikModel model = new TableNastavnikModel(Controller.getInstance().getAllNastavnici());
            tblNastavnici.setModel(model);
        } catch (Exception ex) {
            Logger.getLogger(FormPretraziNastavnike.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void updateNastavnik(Nastavnik novi) {
        TableNastavnikModel model = (TableNastavnikModel) tblNastavnici.getModel();
        model.updateData(novi);
    }

    void addNastavnik(Nastavnik nastavnik) {
        TableNastavnikModel model = (TableNastavnikModel) tblNastavnici.getModel();
        model.addNastavnik(nastavnik);
    }
