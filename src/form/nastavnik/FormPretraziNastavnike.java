    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        int row = tblNastavnici.getSelectedRow();
        if (Validator.getInstance().izabranRed(row)) {
            TableNastavnikModel model = (TableNastavnikModel) tblNastavnici.getModel();
            Nastavnik nastavnik = model.getNastavnik(row);
            Session.getInstance().setTrenutniSlucajKoriscenja(SlucajKoriscenja.SK_PRIKAZI_NASTAVNIKA);
            Session.getInstance().getParametriSK().clear();
            Session.getInstance().getParametriSK().put("nastavnik", nastavnik);
            JDialog nastavnikDialog = new DialogNoviNastavnik(this, true, FormMode.PRIKAZI);
            nastavnikDialog.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Izaberite nastavnika.");
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed

