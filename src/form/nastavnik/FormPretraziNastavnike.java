    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int row = tblNastavnici.getSelectedRow();
        if (Validator.getInstance().izabranRed(row)) {
            TableNastavnikModel model = (TableNastavnikModel) tblNastavnici.getModel();
            Nastavnik nastavnik = model.getNastavnik(row);
            Session.getInstance().setTrenutniSlucajKoriscenja(SlucajKoriscenja.SK_OBRISI_NASTAVNIKA);
            try {
                String poruka = Controller.getInstance().obrisiNastavnika(nastavnik);
                JOptionPane.showMessageDialog(this, poruka);
                model.removeNastavnik(row);
            } catch (Exception ex) {
                Logger.getLogger(FormPretraziNastavnike.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Izaberite nastavnika.");
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

